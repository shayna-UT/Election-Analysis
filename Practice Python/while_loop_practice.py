# we test if x is less than or equal to 5 
while x <= 5:
    # if the condition is true, then we print the value of x
    print(x)
    # with x = x + 1, we increment x by 1
    x = x + 1
# the condition is tested again
# we repeat the process until the condition is false 
# when x > 5, the loop stops 

